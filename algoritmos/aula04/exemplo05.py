x_1 = float(input('Coloque o valor de x1: '))
x_2 = float(input('Coloque o valor de x2: '))
y_1 = float(input('Coloque o valor de y1: '))
y_2 = float(input('Coloque o valor de y2: '))
dist_x = x_2 - x_1
dist_y = y_2 - y_1
dist = (dist_x**2 + dist_y**2)**(1/2)
print('A distância entre os pontos x e y é ', dist)
